    void Load(EStatut statut) {
    List<Etudiant> etudiants = es.findByStatut(statut);
    model.setRowCount(0); 

    for (Etudiant etu : etudiants) {
        model.addRow(new Object[]{etu.getId(), etu.getNom(), etu.getPrenom(), etu.getEmail()});
    }
}

    
