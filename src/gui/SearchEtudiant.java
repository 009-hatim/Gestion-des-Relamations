    private void bnSearcheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnSearcheActionPerformed
        String NomOrEmail = txtNom.getText().toString();
        if (!NomOrEmail.isEmpty()) {
            List<Etudiant> results = (List<Etudiant>) es.findByNameOrEmail(NomOrEmail);
            if (!results.isEmpty()) {
                Etudiant selectedEtudiant = results.get(0);
                Load(selectedEtudiant); 
            } else {
                JOptionPane.showMessageDialog(this, "Aucun étudiant trouvé avec le nom donné.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez saisir un terme de recherche.");
        }

    }//GEN-LAST:event_bnSearcheActionPerformed


