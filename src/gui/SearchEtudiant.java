    void Load(Etudiant etu) {
        // First, clear the previous data in the table
        model.setRowCount(0);
        List<TraitementReclamation> traitementReclamations = trs.findByEtudiant(etu);

        // If the list is not empty, populate the table
        if (!traitementReclamations.isEmpty()) {
            for (TraitementReclamation t : traitementReclamations) {
                // Create a row for each TraitementReclamation object
                model.addRow(new Object[]{
                    t.getEtudiant().getNom() + " " + t.getEtudiant().getPrenom(), // Full name (Nom + Prénom) in the same column
                    t.getReclamation().getObjet(), // Objet of the Reclamation
                    t.getStatut(), // Statut of the TraitementReclamation
                    t.getReclamation().getDate() // Date of the Reclamation
                });
            }
        } else {
            // If no data is found, show a message in the table (optional)
            JOptionPane.showMessageDialog(this, "Aucune réclamation trouvée pour cet étudiant.");
        }
    }

