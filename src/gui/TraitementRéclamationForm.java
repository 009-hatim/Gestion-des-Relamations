    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed
        EStatut statut = null;
        if (oui.isSelected()) {
            statut = EStatut.TRAITEE;
        } else {
            statut = EStatut.NON_TRAITEE;
        }
        String commentaire = txtComment.getText().toString();
        Reclamation reclamation = (Reclamation) listeReclamation.getSelectedItem();
        Etudiant etudiant = (Etudiant) listeEtudiant.getSelectedItem();

        if (trs.create(new TraitementReclamation(statut, commentaire, reclamation, etudiant))) {
            JOptionPane.showMessageDialog(this, "bien enregistrée");
            Load();
        } else {
            JOptionPane.showMessageDialog(this, "Probléme");
        }

    }//GEN-LAST:event_bnAddActionPerformed

    private void bnUpddateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpddateActionPerformed
        int selectedRow = listeTraitement.getSelectedRow();

        if (selectedRow != -1) {
            int response = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier cette réclamation ?");

            if (response == JOptionPane.YES_OPTION) {
                Etudiant etudiant = (Etudiant) listeEtudiant.getSelectedItem();
                Reclamation reclamation = (Reclamation) listeReclamation.getSelectedItem();
                EStatut statut = oui.isSelected() ? EStatut.TRAITEE : EStatut.NON_TRAITEE;
                String commentaire = txtComment.getText();

                TraitementReclamation traitement = new TraitementReclamation(statut, commentaire, reclamation, etudiant);
                if (trs.update(traitement)) {
                    JOptionPane.showMessageDialog(this, "Modification réussie");
                    Load();
                } else {
                    JOptionPane.showMessageDialog(this, "Erreur lors de la modification");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une réclamation à modifier.");
        }
    }//GEN-LAST:event_bnUpddateActionPerformed

    private void listeTraitementMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeTraitementMouseClicked
        int selectedRow = listeTraitement.getSelectedRow();
        if (selectedRow != -1) {
            Etudiant etudiant = (Etudiant) model.getValueAt(selectedRow, 0);
            Reclamation reclamation = (Reclamation) model.getValueAt(selectedRow, 1);
            EStatut statut = (EStatut) model.getValueAt(selectedRow, 2);
            String commentaire = model.getValueAt(selectedRow, 3).toString();

            // Set the retrieved values in form fields
            listeEtudiant.setSelectedItem(etudiant);
            listeReclamation.setSelectedItem(reclamation);
            txtComment.setText(commentaire);

            if (statut == EStatut.TRAITEE) {
                oui.setSelected(true);
            } else {
                non.setSelected(true);
            }
        }
    }//GEN-LAST:event_listeTraitementMouseClicked

    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
        int selectedRow = listeTraitement.getSelectedRow();

        if (selectedRow != -1) {
            int response = JOptionPane.showConfirmDialog(this,
                    "Voulez-vous vraiment supprimer ce traitement de réclamation ?",
                    "Confirmation", JOptionPane.YES_NO_OPTION);

            if (response == JOptionPane.YES_OPTION) {
                Etudiant etudiant = (Etudiant) model.getValueAt(selectedRow, 0);
                Reclamation reclamation = (Reclamation) model.getValueAt(selectedRow, 1);
                EStatut statut = (EStatut) model.getValueAt(selectedRow, 2);
                String commentaire = model.getValueAt(selectedRow, 3).toString();

                TraitementReclamation traitement = new TraitementReclamation(statut, commentaire, reclamation, etudiant);

                if (trs.delete(traitement)) {
                    JOptionPane.showMessageDialog(this, "Suppression réussie");
                    Load(); // Recharger les données
                } else {
                    JOptionPane.showMessageDialog(this, "Erreur lors de la suppression");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un traitement de réclamation à supprimer.");
        }
    }//GEN-LAST:event_bnDeleteActionPerformed


