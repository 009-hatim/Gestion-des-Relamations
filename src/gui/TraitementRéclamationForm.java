    void loadReclamation() {
        for (Reclamation r : rs.findAll()) {
            listeReclamation.addItem(r);
        }
    }

    void loadEtudiant() {
        for (Etudiant e : es.findAll()) {
            listeEtudiant.addItem(e);
        }
    }

    void Load() {
        model.setRowCount(0);
        for (TraitementReclamation t : trs.findAll()) {
            model.addRow(new Object[]{t.getEtudiant(), t.getReclamation(), t.getStatut(), t.getCommentaire()});
        }

    }

